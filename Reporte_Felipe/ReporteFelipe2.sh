SELECT distinct on (organizations.tax_number) organizations.tax_number AS "RUT_EMPRESA",
			   initcap(organizations.name) AS "NOMBRE",
		       users.status as "STATUS",
		       concat(profiles.first_name, ' ', profiles.last_name) as "NOMBRE_USUARIO",
		       users.email as "EMAIL_USUARIO",
		       profiles.job_title as "JOB_TITLE",
		       profiles.phone_number as "TELEFONO",
		       (select max(invoices.issue_date) from "Btg_Btg".invoices where organizations.id = invoices.issuer_id) as "FACTURAS",
		       (select max(payrolls.updated_at) from "Btg_Btg".payrolls where organizations.tax_number = payrolls.organization_tax_number) AS "PREVIRED",
		       (select max(month_tax_reports.date) from "Btg_Btg".month_tax_reports where organizations.tax_number = month_tax_reports.tax_number) AS "F29",
		       min(users.created_at at time zone 'utc' at time zone 'america/santiago') as "FECHA_CREACION",
		       organizations.id as "ID"
		FROM "Btg_Btg".users inner join
		     "Btg_Btg".organizations on organizations.id = users.organization_id
		     left join "Btg_Btg".profiles on profiles.user_id = users.id
		WHERE users.deleted_at IS NULL and organizations.deleted_at IS NULL
		GROUP BY 12, 1, 2, 3, 4, 5, 6, 7
		order by 1
SQL


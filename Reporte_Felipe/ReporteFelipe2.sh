		select distinct organization_tax_number as "RUT_SCRAPPER", max(GREATEST(created_at, updated_at)) as "ULTIMA_ACTUALIZACION",
		max(case when code='PRV' then code else NULL end) as "PREVIRED",
		max(case when code='SII' and owner_type='Organization' then 'SII' else NULL end) as "SII",
		max(case when code!='PRV' and owner_type='User' then code else NULL end) as "FACTURADOR"
